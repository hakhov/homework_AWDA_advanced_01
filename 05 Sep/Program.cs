// Ref with strings.
    
string s = "Mrs Milanda";
GetString(ref s);
Console.WriteLine(s);

static void GetString (ref string s)
{
    s = "Mr Thomas";
}//Ըստ այս օրինակը Ref-ը string-ի հետ աշխատեց նույն տրամաբանությամբ ինչպես որ int-ի։ Վերցրեց s-ի նախնական արժեքը, բայց
// օգտագործեց՝ հիմնականը(փակագծերի փոփոխականը)։ Ու եթե մենք փակագծերում չունենայինք s փոփոխական ապա այն կտպեր ամենավերևի
//"Mrs Milanda"-ն։


string s2 = "Katy ";
GetString2(ref s2);
Console.WriteLine(s2);

static string GetString2(ref string s2)
{
    s2 += 1;
    return s2;
}//Այս օրինակում+= նշանի օգնությամբ մենք միացնում ենք 2 string արժեքները։ Ref-ը վերցնում է "katy"-ի
// օրիգինալ հասցեն (վերևի ու ներքևի s2-ները տարբեր բաներ են) միացնում է երկրորդ s2-ը և արտածում նոր արժեք։



